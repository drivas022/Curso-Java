public class OperadoresAritmeticos {
    public static void main(String[] args) {
        System.out.println("*** Operadores Aritmétcos **");
        int a = 5, b = 3, resultado;
        // Suma
        resultado = a + b;
        System.out.println("resultado = " + resultado);
        // Resta
        resultado = a - b;
        System.out.println("resultado = " + resultado);
        // Multiplicación
        resultado = a * b;
        System.out.println("resultado = " + resultado);
        // División
        resultado = a / b;
        System.out.println("resultado = " + resultado);
        // Modulo (residuo división) %
        resultado = a % b;
        System.out.println("modulo = " + resultado);
    }
}
