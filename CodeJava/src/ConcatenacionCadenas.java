public class ConcatenacionCadenas {
    public static void main(String[] args) {
        System.out.println("*** Concatencion de Cadenas ***");
        var nombre = "Diego";
        var apellido = "Rivas";
        var nombreCompleto = nombre + " " + apellido;
        System.out.println("Nombre completo: " + nombreCompleto);
    }
}
